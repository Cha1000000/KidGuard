package ru.homelab.kidguard.core.domain.model

/**
 * Специальные разрешения детского режима, выдаваемые вручную через системные экраны.
 * Порядок объявления = порядок шагов в мастере первичной настройки.
 */
enum class DevicePermission {
    /** Accessibility-сервис (детект активного приложения, оверлей-блокировка). */
    ACCESSIBILITY,

    /** Рисование поверх других приложений (экраны блокировки и предупреждений). */
    OVERLAY,

    /** Администратор устройства (защита от удаления; НЕ Device Owner). */
    DEVICE_ADMIN,

    /** Исключение из оптимизации батареи (чтобы фоновый контроль не выгружался). */
    BATTERY_OPTIMIZATION,

    /** Показ уведомлений (чтобы было видно уведомление активного контроля). */
    NOTIFICATIONS,

    /** Согласие на VPN (блокировка интернета после лимита; полноценно — веха 5). */
    VPN
}
