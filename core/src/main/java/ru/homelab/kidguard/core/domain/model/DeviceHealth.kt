package ru.homelab.kidguard.core.domain.model

/**
 * Отчёт детского устройства о том, работает ли контроль (watchdog, веха 6).
 *
 * Зачем: контроль умеет умирать МОЛЧА. Реальный случай 16.07.2026 — accessibility был выключен
 * 26 минут, детский экран при этом показывал «2 ч осталось», у родителя просто перестали расти
 * цифры в статистике. Ни ребёнок, ни родитель не узнали бы, что защиты нет.
 *
 * Отчёт уходит на сервер раз в ~15 мин вместе с остальной синхронизацией и виден родителю.
 * Ребёнку не показывается никогда: «контроль не активен» — прямая подсказка «можно гулять».
 *
 * Флаги — «выдано ли разрешение сейчас». Сам факт доставки отчёта важнее его содержимого:
 * молчание дольше порога означает, что сервис убит целиком (вендором, очисткой данных,
 * принудительной остановкой) — такое устройство доложить о себе уже не может.
 */
data class DeviceHealth(
    /** Без него не определяется активное приложение: не идёт учёт времени, не работают блокировки. */
    val accessibility: Boolean,
    /** Без него нельзя показать оверлей блокировки. */
    val overlay: Boolean,
    /** Без него приложение можно удалить. */
    val deviceAdmin: Boolean,
    /** Без него не режется интернет после лимита. */
    val vpn: Boolean,
    /** Без него система выгружает фоновый контроль. */
    val batteryOptimization: Boolean
) {
    /** Всё ли на месте. Если нет — родителю показываем предупреждение. */
    val isHealthy: Boolean
        get() = brokenPermissions().isEmpty()

    /**
     * Что именно отвалилось — родителю показываем не «что-то сломалось», а конкретный список.
     * Порядок как в [DevicePermission]: он же задаёт порядок шагов в мастере разрешений, и родитель
     * увидит проблемы в том же порядке, в каком будет их чинить.
     *
     * [DevicePermission.NOTIFICATIONS] сюда не входит: без уведомления контроль работает, просто
     * менее заметен — это не поломка.
     */
    fun brokenPermissions(): List<DevicePermission> = buildList {
        if (!accessibility) add(DevicePermission.ACCESSIBILITY)
        if (!overlay) add(DevicePermission.OVERLAY)
        if (!deviceAdmin) add(DevicePermission.DEVICE_ADMIN)
        if (!batteryOptimization) add(DevicePermission.BATTERY_OPTIMIZATION)
        if (!vpn) add(DevicePermission.VPN)
    }
}
