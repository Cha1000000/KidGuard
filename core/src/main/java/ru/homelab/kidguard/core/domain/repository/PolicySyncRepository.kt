package ru.homelab.kidguard.core.domain.repository

/**
 * Синхронизация единой политики с сервером (веха 4.3). Сервер — источник истины (LWW по
 * серверному `updatedAt`); локальный Room — исполнитель, работающий офлайн (вехи 2–3).
 *
 * Обе петли работают, пока жива вызывающая корутина, и молча переживают сетевые ошибки
 * (повтор на следующей итерации) — синхронизация не должна ронять UI или сервис.
 */
interface PolicySyncRepository {

    /**
     * Петля родителя: разовый pull при входе (подхватить правки второго родителя), затем
     * наблюдение локальных правок политики и push на сервер с дебаунсом. Активный ребёнок —
     * первый в списке `/children` (селектор для нескольких детей — отложен).
     */
    suspend fun parentSyncLoop()

    /**
     * Петля детского устройства: pull политики своего ребёнка при старте и далее периодически;
     * применяет документ в локальный Room целиком (правила тут же исполняются офлайн-движком).
     */
    suspend fun childSyncLoop()
}
