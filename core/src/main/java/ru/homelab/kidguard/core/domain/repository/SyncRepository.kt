package ru.homelab.kidguard.core.domain.repository

import kotlinx.coroutines.flow.Flow

/**
 * Синхронизация с сервером: единая политика (веха 4.3) и статистика использования (веха 4.4).
 * По политике сервер — источник истины (LWW по серверному `updatedAt`), Room — офлайн-исполнитель;
 * по статистике источник истины — детское устройство (шлёт абсолютные накопленные значения).
 *
 * Обе петли работают, пока жива вызывающая корутина, и молча переживают сетевые ошибки
 * (повтор на следующей итерации) — синхронизация не должна ронять UI или сервис.
 */
interface SyncRepository {

    /**
     * Петля родителя: разовый pull политики при входе (подхватить правки второго родителя),
     * затем наблюдение локальных правок и push на сервер с дебаунсом. Push/pull идут для
     * активного ребёнка ([activeChildId]; по умолчанию — первый в списке `/children`).
     */
    suspend fun parentSyncLoop()

    /**
     * Активный ребёнок родителя (веха 4.5): выбор общий для всего приложения и переживает
     * перезапуск. `null` — выбор ещё не сделан (тогда петля берёт первого из списка и сохраняет).
     */
    val activeChildId: Flow<Int?>

    /**
     * Переключить активного ребёнка: забрать с сервера ЕГО политику и целиком заменить ею
     * локальную (Room у родителя — кэш активного ребёнка). Требует сети; при ошибке выбор
     * не меняется и локальные данные остаются прежними.
     */
    suspend fun switchActiveChild(childId: Int): Result<Unit>

    /**
     * Петля детского устройства, периодически: pull политики (применяется в Room целиком,
     * правила тут же исполняются офлайн-движком) + push статистики за сегодня и вчера.
     */
    suspend fun childSyncLoop()
}
