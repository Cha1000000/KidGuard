package ru.homelab.kidguard.core.domain.model

/**
 * Роль устройства в системе KidGuard. Выбирается один раз при первичной настройке
 * и фиксируется навсегда (см. концепцию: без повторного логина и смены роли).
 */
enum class Role {
    /** Телефон родителя — управление правилами и просмотр статистики. */
    PARENT,

    /** Телефон ребёнка — исполнение правил контроля. */
    CHILD
}
