package ru.homelab.kidguard.platform.overlay

/** Причина показа блокирующего оверлея — определяет заголовок и подзаголовок экрана. */
enum class BlockReason {
    /** Исчерпан лимит времени (общий или личный лимит приложения). */
    LIMIT_EXPIRED,

    /** Приложение полностью запрещено родителем (веха 4.1.2) — блокируется вне зависимости от лимитов. */
    BLOCKED_BY_PARENT
}
